    private boolean validateName() {
        // Regex for names (only characters)
        String nameRegex = "^[a-zA-Z\\s]+$";
        if (!name.getText().matches(nameRegex)) {
            JOptionPane.showMessageDialog(null, "Invalid name. Please enter only characters for the name.");
            return false;
        }
        return true;
    }

