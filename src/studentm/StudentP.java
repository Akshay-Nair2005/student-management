    private boolean validatePhoneNumber() {
        // Regex for phone number (only integers)
        String phoneRegex = "^[0-9]{10}$";

        if (!phoneno.getText().matches(phoneRegex)) {
            JOptionPane.showMessageDialog(null, "Invalid phone number. Please enter exactly 10 digits and only integers.");
            return false;
        }
        return true;
    }

    private boolean validateEmail() {
        // Regex for email
        String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        if (!email.getText().matches(emailRegex)) {
            JOptionPane.showMessageDialog(null, "Invalid email. Please enter a valid email address.");
            return false;
        }
        return true;
    }

    private boolean validateAddress() {
        // Regex for address (only characters)
        String addressRegex = "^[a-zA-Z\\s]+$";
        if (!add.getText().matches(addressRegex)) {
            JOptionPane.showMessageDialog(null, "Invalid address. Please enter only characters for the address.");
            return false;
        }
        return true;
    }

